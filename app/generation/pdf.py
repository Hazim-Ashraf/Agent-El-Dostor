"""Render a GeneratedContract to a correctly-formatted PDF.

WeasyPrint (HTML/CSS -> PDF, backed by Pango/HarfBuzz) does the hard part: it shapes
Arabic and reorders RTL runs correctly when the element is `dir="rtl"` with an Arabic
font (Amiri). We build semantic HTML and let it typeset.

Layouts:
  - Arabic-only / English-only  -> one single-column PDF.
  - Bilingual, "side_by_side"    -> one PDF, each clause EN (left) | AR (right).
  - Bilingual, "sequential"      -> one PDF, full EN section then full AR section.
  - Bilingual, "separate"        -> two PDFs (one EN, one AR).
"""
from __future__ import annotations

import html as _html
from typing import Any

from app.generation.schema import GeneratedContract

# --------------------------------------------------------------------------- #
# Localised labels / boilerplate
# --------------------------------------------------------------------------- #
